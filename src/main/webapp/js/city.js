function chsel(){		
	var pro = document.getElementById("provid");
	var city = document.getElementById("cityid");
	var provinces = document.getElementById('provinces');
	if(pro.value) {
		city.options.length=0;	
		for(var i=0;i<selects[pro.value].length;i++){
			city.options.add(selects[pro.value][i]);
		}	
	}
	if(provinces){
		provinces.value = pro.options[pro.selectedIndex].text + "," + city.options[city.selectedIndex].text;
	}
}
