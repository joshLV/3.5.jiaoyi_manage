package cn.juhaowan.count.vo;

public class CountBuySeller {
	private CountBuyData buyData;
	private CountDayUser dayUser;
	private CountSellData sellData;
	
	public CountBuyData getBuyData() {
		return buyData;
	}
	public void setBuyData(CountBuyData buyData) {
		this.buyData = buyData;
	}
	public CountDayUser getDayUser() {
		return dayUser;
	}
	public void setDayUser(CountDayUser dayUser) {
		this.dayUser = dayUser;
	}
	public CountSellData getSellData() {
		return sellData;
	}
	public void setSellData(CountSellData sellData) {
		this.sellData = sellData;
	}
	

}
